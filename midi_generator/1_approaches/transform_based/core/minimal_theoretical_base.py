class Quantize16thTransform(SpaceLevelTransform):
    """
    Quantize16: Quantize to 16th note grid.

    Essential for MIDI that's quantized to 16th notes.
    Generator of all grid-based rhythms.

    Mathematical: Q: ℝ → (1/16)ℤ (snap to grid)
    """

    def __init__(self):
        metadata = TransformMetadata(
            name='quantize_16th',
            dimension='rhythm',
            level='note',
            description='Quantize to 16th note grid',
            parameter_range=(0.0, 1.0),
            default_value=1.0,  # Full quantization
            is_invertible=False
        )
        super().__init__(metadata)

    def apply(self, midi: mido.MidiFile, amount: float) -> mido.MidiFile:
        """Apply quantization"""
        amount = np.clip(amount, 0.0, 1.0)

        if amount < 0.1:
            return midi

        notes = extract_notes_from_midi(midi)

        grid_size = 0.125  # 16th note at 120 BPM

        for note in notes:
            # Quantize start time
            quantized_start = round(note['start_time'] / grid_size) * grid_size
            # Blend between original and quantized
            note['start_time'] = (
                (1 - amount) * note['start_time'] +
                amount * quantized_start
            )

            # Quantize duration
            quantized_dur = round(note['duration'] / grid_size) * grid_size
            quantized_dur = max(grid_size, quantized_dur)  # At least one grid unit
            note['duration'] = (
                (1 - amount) * note['duration'] +
                amount * quantized_dur
            )

        return notes_to_midi(notes, midi.ticks_per_beat)

    def get_current_value(self, midi: mido.MidiFile) -> float:
        """Estimate quantization amount"""
        notes = extract_notes_from_midi(midi)
        if len(notes) == 0:
            return 1.0

        grid_size = 0.125
        # Check how many notes are on-grid
        on_grid = sum(
            1 for n in notes
            if abs(n['start_time'] % grid_size) < 0.01
        )

        return on_grid / len(notes)


