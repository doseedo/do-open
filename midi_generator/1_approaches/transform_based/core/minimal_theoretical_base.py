class RetrogradeTransform(SpaceLevelTransform):
    """
    R: Retrograde (time reversal).

    Play the piece backwards in time.
    This is TIME INVERSION - the dual of pitch inversion.

    Mathematical: R: Time → Time, t ↦ T_max - t
    where T_max is the total duration.

    Property: R ∘ R = identity (involution)
    """

    def __init__(self):
        metadata = TransformMetadata(
            name='retrograde',
            dimension='rhythm',
            level='section',
            description='Retrograde (time reversal)',
            parameter_range=(0.0, 1.0),
            default_value=0.0,  # No retrograde
            is_invertible=True
        )
        super().__init__(metadata)

    def apply(self, midi: mido.MidiFile, amount: float) -> mido.MidiFile:
        """Apply retrograde"""
        amount = np.clip(amount, 0.0, 1.0)

        if amount < 0.1:
            return midi

        notes = extract_notes_from_midi(midi)

        if len(notes) == 0:
            return midi

        # Find max time
        max_time = max(n['start_time'] + n['duration'] for n in notes)

        # Reverse time
        for note in notes:
            note['start_time'] = max_time - (note['start_time'] + note['duration'])

        # Sort by new start time
        notes.sort(key=lambda n: n['start_time'])

        return notes_to_midi(notes, midi.ticks_per_beat)

    def get_current_value(self, midi: mido.MidiFile) -> float:
        """Cannot detect retrograde without reference"""
        return 0.0


class TimeScaleTransform(SpaceLevelTransform):
    """
    TimeStretch: Uniform time scaling.

    Speed up or slow down uniformly.
    Generator of all rhythmic augmentation/diminution.

    Mathematical: S_r: Time → Time, t ↦ r·t
    where r is the scaling ratio.

    Special cases:
    - S_2 = rhythmic augmentation (twice as slow)
    - S_0.5 = rhythmic diminution (twice as fast)
    """

    def __init__(self):
        metadata = TransformMetadata(
            name='time_scale',
            dimension='rhythm',
            level='section',
            description='Uniform time scaling (augmentation/diminution)',
            parameter_range=(0.25, 4.0),  # 0.25x to 4x speed
            default_value=1.0,  # No change
            is_invertible=True
        )
        super().__init__(metadata)

    def apply(self, midi: mido.MidiFile, amount: float) -> mido.MidiFile:
        """Apply time scaling"""
        amount = np.clip(amount, 0.25, 4.0)

        notes = extract_notes_from_midi(midi)

        for note in notes:
            note['start_time'] *= amount
            note['duration'] *= amount

        return notes_to_midi(notes, midi.ticks_per_beat)

    def get_current_value(self, midi: mido.MidiFile) -> float:
        """Estimate time scale (always 1.0 without reference)"""
        return 1.0


class TimeShiftTransform(SpaceLevelTransform):
    """
    TimeOffset: Shift in time (translation).

    Move all events forward or backward in time.
    Generator of all temporal translations.

    Mathematical: O_t: Time → Time, t' ↦ t' + t
    """

    def __init__(self):
        metadata = TransformMetadata(
            name='time_shift',
            dimension='rhythm',
            level='section',
            description='Shift all events in time',
            parameter_range=(-10.0, 10.0),  # ±10 seconds
            default_value=0.0,  # No shift
            is_invertible=True
        )
        super().__init__(metadata)

    def apply(self, midi: mido.MidiFile, amount: float) -> mido.MidiFile:
        """Apply time shift"""
        amount = np.clip(amount, -10.0, 10.0)

        notes = extract_notes_from_midi(midi)

        for note in notes:
            note['start_time'] += amount
            note['start_time'] = max(0.0, note['start_time'])

        return notes_to_midi(notes, midi.ticks_per_beat)

    def get_current_value(self, midi: mido.MidiFile) -> float:
        """Get time offset"""
        notes = extract_notes_from_midi(midi)
        if len(notes) == 0:
            return 0.0

        # Minimum start time
        return min(n['start_time'] for n in notes)


