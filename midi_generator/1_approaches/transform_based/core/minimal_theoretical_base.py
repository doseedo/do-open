class RepeatTransform(SpaceLevelTransform):
    """
    Repeat: Exact repetition.

    Fundamental structural operation.
    Generator of all periodic structures.

    Mathematical: Repeat_n: X → X^n (Cartesian power)
    """

    def __init__(self):
        metadata = TransformMetadata(
            name='repeat',
            dimension='form',
            level='section',
            description='Exact repetition',
            parameter_range=(1.0, 8.0),  # 1-8 repetitions
            default_value=1.0,
            is_invertible=False  # Can't undo repetition
        )
        super().__init__(metadata)

    def apply(self, midi: mido.MidiFile, amount: float) -> mido.MidiFile:
        """Apply repetition"""
        amount = np.clip(amount, 1.0, 8.0)
        n_repeats = int(round(amount))

        if n_repeats <= 1:
            return midi

        notes = extract_notes_from_midi(midi)

        if len(notes) == 0:
            return midi

        # Duration of section
        max_time = max(n['start_time'] + n['duration'] for n in notes)

        # Repeat
        repeated_notes = []
        for i in range(n_repeats):
            for note in notes:
                repeated_note = note.copy()
                repeated_note['start_time'] += i * max_time
                repeated_notes.append(repeated_note)

        return notes_to_midi(repeated_notes, midi.ticks_per_beat)

    def get_current_value(self, midi: mido.MidiFile) -> float:
        """Cannot detect repetition without analysis"""
        return 1.0


class FragmentTransform(SpaceLevelTransform):
    """
    Fragment: Take subset (first n%).

    Dual of repetition.
    Generator of all truncation operations.

    Mathematical: Fragment_r: X → X[0:r] (prefix)
    """

    def __init__(self):
        metadata = TransformMetadata(
            name='fragment',
            dimension='form',
            level='section',
            description='Take first n% of material',
            parameter_range=(0.1, 1.0),
            default_value=1.0,  # Full piece
            is_invertible=False
        )
        super().__init__(metadata)

    def apply(self, midi: mido.MidiFile, amount: float) -> mido.MidiFile:
        """Apply fragmentation"""
        amount = np.clip(amount, 0.1, 1.0)

        notes = extract_notes_from_midi(midi)

        if len(notes) == 0:
            return midi

        # Duration of section
        max_time = max(n['start_time'] + n['duration'] for n in notes)
        cutoff = max_time * amount

        # Keep only notes starting before cutoff
        fragmented = [n for n in notes if n['start_time'] < cutoff]

        return notes_to_midi(fragmented, midi.ticks_per_beat)

    def get_current_value(self, midi: mido.MidiFile) -> float:
        """Always returns 1.0 (full piece)"""
        return 1.0


