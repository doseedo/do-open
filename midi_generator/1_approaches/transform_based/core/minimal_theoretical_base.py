class TransposeSemitoneTransform(SpaceLevelTransform):
    """
    T_1: Transpose by one semitone (generator of T_n group).

    All transpositions T_0 through T_11 can be generated by composition:
    - T_0 = identity
    - T_2 = T_1 ∘ T_1
    - T_3 = T_1 ∘ T_1 ∘ T_1
    - ...
    - T_11 = T_1 composed 11 times

    This is the MINIMAL generator of the cyclic transposition group.

    Mathematical: T_1: ℤ/12ℤ → ℤ/12ℤ, n ↦ n+1 (mod 12)
    """

    def __init__(self):
        metadata = TransformMetadata(
            name='transpose_semitone',
            dimension='pitch',
            level='note',
            description='Transpose up by one semitone (T_1 generator)',
            parameter_range=(0.0, 11.0),  # 0-11 semitones
            default_value=0.0,  # Identity
            is_invertible=True
        )
        super().__init__(metadata)

    def apply(self, midi: mido.MidiFile, amount: float) -> mido.MidiFile:
        """Apply transposition by amount semitones"""
        amount = np.clip(amount, 0.0, 11.0)
        semitones = int(round(amount))

        midi_copy = copy.deepcopy(midi)

        for track in midi_copy.tracks:
            for msg in track:
                if msg.type in ('note_on', 'note_off'):
                    msg.note = np.clip(msg.note + semitones, 0, 127)

        return midi_copy

    def get_current_value(self, midi: mido.MidiFile) -> float:
        """Get transposition level (0-11)"""
        notes = extract_notes_from_midi(midi)
        if len(notes) == 0:
            return 0.0

        # Calculate mean pitch class
        mean_pc = np.mean([n['pitch'] % 12 for n in notes])

        # Estimate transposition from C (0)
        return mean_pc


class InversionTransform(SpaceLevelTransform):
    """
    I: Inversion around pitch class 0 (C).

    Musical inversion: I(p) = -p (mod 12)
    All inversions I_0 through I_11 can be generated:
    - I_0 = I around C
    - I_1 = T_1 ∘ I (inversion around C#)
    - I_2 = T_2 ∘ I (inversion around D)
    - ...

    This is the REFLECTION operation in pitch-class space.

    Mathematical: I: ℤ/12ℤ → ℤ/12ℤ, n ↦ -n (mod 12)
    """

    def __init__(self):
        metadata = TransformMetadata(
            name='inversion',
            dimension='pitch',
            level='note',
            description='Inversion around C (I_0)',
            parameter_range=(0.0, 1.0),
            default_value=0.0,  # No inversion
            is_invertible=True
        )
        super().__init__(metadata)

    def apply(self, midi: mido.MidiFile, amount: float) -> mido.MidiFile:
        """Apply inversion (amount determines blend)"""
        amount = np.clip(amount, 0.0, 1.0)

        if amount < 0.1:
            return midi  # No inversion

        notes = extract_notes_from_midi(midi)

        # Find center pitch for inversion
        pitches = [n['pitch'] for n in notes]
        center = np.mean(pitches)

        # Invert around center
        for note in notes:
            distance = note['pitch'] - center
            inverted_pitch = center - distance
            # Blend between original and inverted
            note['pitch'] = int(
                (1 - amount) * note['pitch'] + amount * inverted_pitch
            )
            note['pitch'] = np.clip(note['pitch'], 0, 127)

        return notes_to_midi(notes, midi.ticks_per_beat)

    def get_current_value(self, midi: mido.MidiFile) -> float:
        """Estimate if inverted (0=no, 1=yes)"""
        notes = extract_notes_from_midi(midi)
        if len(notes) < 2:
            return 0.0

        # Check if pitch contour is inverted
        # (Simplified - would need reference)
        return 0.0


