class ParallelTransform(SpaceLevelTransform):
    """
    P: Parallel transformation (Major ↔ Minor).

    Neo-Riemannian P operation:
    - C major (C-E-G) → C minor (C-Eb-G)
    - Preserves root and fifth, changes third

    Mathematical: P inverts the third of a triad
    Property: P ∘ P = identity (involution)

    Reference: Cohn (1996) "Maximally Smooth Cycles"
    """

    def __init__(self):
        metadata = TransformMetadata(
            name='parallel',
            dimension='harmony',
            level='phrase',
            description='Parallel (Major ↔ Minor)',
            parameter_range=(0.0, 1.0),
            default_value=0.0,
            is_invertible=True
        )
        super().__init__(metadata)

    def apply(self, midi: mido.MidiFile, amount: float) -> mido.MidiFile:
        """Apply parallel transformation"""
        amount = np.clip(amount, 0.0, 1.0)

        if amount < 0.1:
            return midi

        notes = extract_notes_from_midi(midi)

        # Identify triads and transform thirds
        # (Simplified implementation)
        for note in notes:
            pc = note['pitch'] % 12
            # If likely a third (4 or 3 semitones from root)
            if pc in [3, 4]:
                # Invert: major third (4) ↔ minor third (3)
                if pc == 4:
                    note['pitch'] -= 1  # Major → Minor
                else:
                    note['pitch'] += 1  # Minor → Major

        return notes_to_midi(notes, midi.ticks_per_beat)

    def get_current_value(self, midi: mido.MidiFile) -> float:
        """Estimate major/minor (0=major, 1=minor)"""
        notes = extract_notes_from_midi(midi)
        if len(notes) == 0:
            return 0.0

        # Count major vs minor thirds
        thirds = [n['pitch'] % 12 for n in notes if (n['pitch'] % 12) in [3, 4]]
        if len(thirds) == 0:
            return 0.0

        minor_ratio = sum(1 for t in thirds if t == 3) / len(thirds)
        return minor_ratio


