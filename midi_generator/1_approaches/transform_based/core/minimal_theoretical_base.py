class VelocityScaleTransform(SpaceLevelTransform):
    """
    VelocityScale: Louder/softer.

    Uniform dynamics scaling.
    Generator of all dynamic transformations.

    Mathematical: V_s: Velocity → Velocity, v ↦ s·v
    """

    def __init__(self):
        metadata = TransformMetadata(
            name='velocity_scale',
            dimension='expression',
            level='section',
            description='Scale velocity (louder/softer)',
            parameter_range=(0.25, 4.0),
            default_value=1.0,
            is_invertible=True
        )
        super().__init__(metadata)

    def apply(self, midi: mido.MidiFile, amount: float) -> mido.MidiFile:
        """Apply velocity scaling"""
        amount = np.clip(amount, 0.25, 4.0)

        notes = extract_notes_from_midi(midi)

        for note in notes:
            note['velocity'] = int(note['velocity'] * amount)
            note['velocity'] = np.clip(note['velocity'], 1, 127)

        return notes_to_midi(notes, midi.ticks_per_beat)

    def get_current_value(self, midi: mido.MidiFile) -> float:
        """Get average velocity scale"""
        notes = extract_notes_from_midi(midi)
        if len(notes) == 0:
            return 1.0

        mean_vel = np.mean([n['velocity'] for n in notes])
        # Normalize to 1.0 for standard velocity (64)
        return mean_vel / 64.0


