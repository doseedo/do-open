#!/usr/bin/env python3
"""
v9 Dataset: Pre-computed Paired Latents from Full Audio Files

Loads pairs generated by preprocess_full_audio.py for fast training.
No on-the-fly encoding - all latents are pre-computed.
"""

import json
import random
from pathlib import Path
from typing import Dict, List

import torch
from torch.utils.data import Dataset


class FullPairedDataset(Dataset):
    """
    Dataset for pre-computed formant shift pairs from full audio files.

    Each sample contains:
    - input: formant-shifted latent (what we want to correct)
    - target: natural latent (what we want to match)
    - source_group: pitch group (0-2)
    - direction: shift direction (0=down, 1=up)
    """

    def __init__(
        self,
        manifest_path: str = '/mnt/msdd2/pitchshift_v9_full_paired/manifest.json',
        window_frames: int = 6,
        samples_per_epoch: int = 10000,
        augment: bool = True,
    ):
        self.window_frames = window_frames
        self.samples_per_epoch = samples_per_epoch
        self.augment = augment

        # Load manifest
        print(f"Loading manifest: {manifest_path}")
        with open(manifest_path) as f:
            self.entries = json.load(f)

        print(f"Found {len(self.entries)} paired samples")

        # Group by direction for balanced sampling
        self.by_direction = {0: [], 1: []}
        for i, e in enumerate(self.entries):
            self.by_direction[e['direction']].append(i)

        print(f"  Direction 0 (down): {len(self.by_direction[0])}")
        print(f"  Direction 1 (up): {len(self.by_direction[1])}")

    def __len__(self):
        return self.samples_per_epoch

    def _get_sample(self) -> Dict:
        """Get a single training sample."""
        for _ in range(10):  # retries
            try:
                # Balanced direction sampling
                direction = random.choice([0, 1])
                if self.by_direction[direction]:
                    idx = random.choice(self.by_direction[direction])
                else:
                    idx = random.randint(0, len(self.entries) - 1)

                entry = self.entries[idx]

                # Load pre-computed pair
                pair = torch.load(entry['pair_path'], weights_only=True)

                shifted = pair['shifted']  # [C, H, T]
                natural = pair['natural']
                group = pair['group']
                direction = pair['direction']

                # Random window
                C, H, T = shifted.shape
                if T < self.window_frames:
                    continue

                if self.augment:
                    start = random.randint(0, T - self.window_frames)
                else:
                    start = 0

                end = start + self.window_frames

                return {
                    'input': shifted[:, :, start:end],
                    'target': natural[:, :, start:end],
                    'source_group': group - 1,  # 0-indexed
                    'direction': direction,
                    'is_paired': True,
                    'valid': True,
                }

            except Exception as e:
                continue

        # Fallback
        return {
            'input': torch.zeros(8, 16, self.window_frames),
            'target': torch.zeros(8, 16, self.window_frames),
            'source_group': 0,
            'direction': 0,
            'is_paired': False,
            'valid': False,
        }

    def __getitem__(self, idx):
        return self._get_sample()


if __name__ == "__main__":
    # Test dataset
    ds = FullPairedDataset(
        manifest_path='/mnt/msdd2/pitchshift_v9_full_paired/manifest.json',
        samples_per_epoch=20,
    )

    print(f"\nTesting {len(ds)} samples...")
    valid = 0
    group_counts = {0: 0, 1: 0, 2: 0}
    dir_counts = {0: 0, 1: 0}

    for i in range(len(ds)):
        sample = ds[i]
        if sample['valid']:
            valid += 1
            group_counts[sample['source_group']] += 1
            dir_counts[sample['direction']] += 1
            if i < 5:
                print(f"  {i}: group={sample['source_group']}, dir={sample['direction']}, shape={sample['input'].shape}")

    print(f"\nValid samples: {valid}/{len(ds)}")
    print(f"Group distribution: {group_counts}")
    print(f"Direction distribution: {dir_counts}")
